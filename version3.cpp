class Point3D {
public:
    double x, y, z;

    Point3D(double x = 0, double y = 0, double z = 0) : x(x), y(y), z(z) {}

    bool operator==(const Point3D& other) const {
        return (abs(x - other.x) < 1e-9 && abs(y - other.y) < 1e-9 && abs(z - other.z) < 1e-9);
    }
};

class Triangle {
public:
    Point3D p1, p2, p3;

    Triangle(Point3D p1, Point3D p2, Point3D p3) : p1(p1), p2(p2), p3(p3) {}
};

class Face {
public:
    vector<Point3D> vertices;

    Face() {}
    
    void addVertex(const Point3D& vertex) {
        vertices.push_back(vertex);
    }
    
    vector<Triangle> triangulate() const {
        vector<Triangle> triangles;
        if (vertices.size() < 3) return triangles;
        
        // Fan triangulation from first vertex
        for (size_t i = 1; i < vertices.size() - 1; i++) {
            triangles.push_back(Triangle(vertices[0], vertices[i], vertices[i + 1]));
        }
        return triangles;
    }
};

Point3D get_random_point_3d(double xmin, double ymin, double zmin, double xmax, double ymax, double zmax) {
    double x = xmin + static_cast<double>(rand()) / (static_cast<double>(RAND_MAX / (xmax - xmin)));
    double y = ymin + static_cast<double>(rand()) / (static_cast<double>(RAND_MAX / (ymax - ymin)));
    double z = zmin + static_cast<double>(rand()) / (static_cast<double>(RAND_MAX / (zmax - zmin)));
    return Point3D(x, y, z);
}

bool cube_contains_surface(double (*f)(double, double, double), Point3D start, Point3D end) {
    vector<Point3D> random_samples;
    for (int i = 0; i < 10000; ++i) {
        random_samples.push_back(get_random_point_3d(start.x, start.y, start.z, end.x, end.y, end.z));
    }

    bool has_positive = false;
    bool has_negative = false;
    for (const auto& p : random_samples) {
        double value = f(p.x, p.y, p.z);
        if (value >= 0) {
            has_positive = true;
        } else if (value < 0) {
            has_negative = true;
        }
        if (has_positive && has_negative) {
            return true;
        }
    }

    return false;
}


Point3D interpolate_3d(Point3D p1, Point3D p2, double f1, double f2) {
    // retornar punto medio
    if (abs(f2 - f1) < 1e-9) {
        return Point3D((p1.x + p2.x) / 2, (p1.y + p2.y) / 2, (p1.z + p2.z) / 2);
    }
    double t = (0 - f1) / (f2 - f1);
    return Point3D(p1.x + t * (p2.x - p1.x), p1.y + t * (p2.y - p1.y), p1.z + t * (p2.z - p1.z));
}

