const double EPS = 1e-8;

struct point3d {
    double x, y, z;
    
    bool operator==(const point3d& other) const {
        return abs(x - other.x) < EPS && abs(y - other.y) < EPS && abs(z - other.z) < EPS;
    }
    
    bool operator!=(const point3d& other) const {
        return !(*this == other);
    }
};

struct triangle {
    point3d v0, v1, v2;
};

point3d interpolate(point3d a, point3d b, double fa, double fb) {
    if (abs(fa - fb) < EPS) {
        return a;
    }
    double t = fa / (fa - fb);
    return {
        a.x + t * (b.x - a.x),
        a.y + t * (b.y - a.y),
        a.z + t * (b.z - a.z)
    };
}


