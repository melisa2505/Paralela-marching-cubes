void marching_cubes(function<double(double, double, double)> f, 
                   point3d vertices[8], vector<triangle>& triangles) {
    // Evaluar función en los 8 vértices del cubo
    double values[8];
    for (int i = 0; i < 8; i++) {
        values[i] = f(vertices[i].x, vertices[i].y, vertices[i].z);
    }
    
    // Calcular configuración del cubo (8 bits)
    int cube_config = 0;
    for (int i = 0; i < 8; i++) {
        if (values[i] > 0) {
            cube_config |= (1 << i);
        }
    }
    
    // Si no hay intersección, salir
    if (edgeTable[cube_config] == 0) {
        return;
    }
    
    // Calcular puntos de intersección en las aristas
    point3d edge_points[12];
    
    // Mapeo de aristas: cada arista conecta dos vértices del cubo
    // Arista 0: vértice 0-1, Arista 1: vértice 1-2, etc.
    if (edgeTable[cube_config] & 1) 
        edge_points[0] = interpolate(vertices[0], vertices[1], values[0], values[1]);
    if (edgeTable[cube_config] & 2) 
        edge_points[1] = interpolate(vertices[1], vertices[2], values[1], values[2]);
    if (edgeTable[cube_config] & 4) 
        edge_points[2] = interpolate(vertices[2], vertices[3], values[2], values[3]);
    if (edgeTable[cube_config] & 8) 
        edge_points[3] = interpolate(vertices[3], vertices[0], values[3], values[0]);
    if (edgeTable[cube_config] & 16) 
        edge_points[4] = interpolate(vertices[4], vertices[5], values[4], values[5]);
    if (edgeTable[cube_config] & 32) 
        edge_points[5] = interpolate(vertices[5], vertices[6], values[5], values[6]);
    if (edgeTable[cube_config] & 64) 
        edge_points[6] = interpolate(vertices[6], vertices[7], values[6], values[7]);
    if (edgeTable[cube_config] & 128) 
        edge_points[7] = interpolate(vertices[7], vertices[4], values[7], values[4]);
    if (edgeTable[cube_config] & 256) 
        edge_points[8] = interpolate(vertices[0], vertices[4], values[0], values[4]);
    if (edgeTable[cube_config] & 512) 
        edge_points[9] = interpolate(vertices[1], vertices[5], values[1], values[5]);
    if (edgeTable[cube_config] & 1024) 
        edge_points[10] = interpolate(vertices[2], vertices[6], values[2], values[6]);
    if (edgeTable[cube_config] & 2048) 
        edge_points[11] = interpolate(vertices[3], vertices[7], values[3], values[7]);
    
    // Crear triángulos usando la tabla de triangulación
    for (int i = 0; triTable[cube_config][i] != -1; i += 3) {
        triangle tri;
        tri.v0 = edge_points[triTable[cube_config][i]];
        tri.v1 = edge_points[triTable[cube_config][i+1]];
        tri.v2 = edge_points[triTable[cube_config][i+2]];
        triangles.push_back(tri);
    }
}

bool is_positive(double x) { return x > EPS; }
bool is_negative(double x) { return x < -EPS; }
bool is_zero(double x) { return !is_positive(x) && !is_negative(x); }

void recursive_marching_cubes(function<double(double, double, double)> f,
                             double x_min, double y_min, double z_min,
                             double x_max, double y_max, double z_max,
                             double precision, vector<triangle>& triangles) {
    
    if (x_max - x_min < precision && y_max - y_min < precision && z_max - z_min < precision) {
        // Crear los 8 vértices del cubo
        point3d vertices[8] = {
            {x_min, y_min, z_min}, // 0
            {x_max, y_min, z_min}, // 1
            {x_max, y_max, z_min}, // 2
            {x_min, y_max, z_min}, // 3
            {x_min, y_min, z_max}, // 4
            {x_max, y_min, z_max}, // 5
            {x_max, y_max, z_max}, // 6
            {x_min, y_max, z_max}  // 7
        };
        
        marching_cubes(f, vertices, triangles);
        return;
    }
    
    double dx = (x_max - x_min) / 2;
    double dy = (y_max - y_min) / 2;
    double dz = (z_max - z_min) / 2;
    
    for (int i = 0; i < 2; ++i) {
        for (int j = 0; j < 2; ++j) {
            for (int k = 0; k < 2; ++k) {
                double x0 = x_min + i * dx;
                double y0 = y_min + j * dy;
                double z0 = z_min + k * dz;
                double x1 = x0 + dx;
                double y1 = y0 + dy;
                double z1 = z0 + dz;
                
                // Verificar si todos los valores en este subcubo tienen el mismo signo
                point3d vertices[8] = {
                    {x0, y0, z0}, {x1, y0, z0}, {x1, y1, z0}, {x0, y1, z0},
                    {x0, y0, z1}, {x1, y0, z1}, {x1, y1, z1}, {x0, y1, z1}
                };
                
                bool all_pos = true, all_neg = true;
                for (int v = 0; v < 8; v++) {
                    double value = f(vertices[v].x, vertices[v].y, vertices[v].z);
                    if (is_positive(value)) {
                        all_neg = false;
                    } else if (is_negative(value)) {
                        all_pos = false;
                    }
                }
                
                // Muestreo adicional para mayor precisión
                for (int sample = 0; sample < 5 && (!all_pos && !all_neg); sample++) {
                    double rx = x0 + (x1 - x0) * (rand() / (double)RAND_MAX);
                    double ry = y0 + (y1 - y0) * (rand() / (double)RAND_MAX);
                    double rz = z0 + (z1 - z0) * (rand() / (double)RAND_MAX);
                    double value = f(rx, ry, rz);
                    
                    if (is_positive(value)) {
                        all_neg = false;
                    } else if (is_negative(value)) {
                        all_pos = false;
                    }
                }
                
                if (all_pos || all_neg) continue;
                
                recursive_marching_cubes(f, x0, y0, z0, x1, y1, z1, precision, triangles);
            }
        }
    }
}

void write_ply(const string& filename, const vector<triangle>& triangles) {
    ofstream file(filename);
    if (!file.is_open()) {
        cerr << "Error opening file: " << filename << endl;
        return;
    }
    
    // Crear lista única de vértices
    vector<point3d> vertices;
    vector<vector<int>> faces;
    
    for (const auto& tri : triangles) {
        vector<int> face_indices;
        
        // Para cada vértice del triángulo
        point3d tri_vertices[3] = {tri.v0, tri.v1, tri.v2};
        for (int i = 0; i < 3; i++) {
            // Buscar si el vértice ya existe
            int index = -1;
            for (int j = 0; j < vertices.size(); j++) {
                if (vertices[j] == tri_vertices[i]) {
                    index = j;
                    break;
                }
            }
            
            // Si no existe, agregarlo
            if (index == -1) {
                vertices.push_back(tri_vertices[i]);
                index = vertices.size() - 1;
            }
            
            face_indices.push_back(index);
        }
        
        faces.push_back(face_indices);
    }
    
    // Escribir header PLY
    file << "ply\n";
    file << "format ascii 1.0\n";
    file << "element vertex " << vertices.size() << "\n";
    file << "property float x\n";
    file << "property float y\n";
    file << "property float z\n";
    file << "element face " << faces.size() << "\n";
    file << "property list uchar int vertex_indices\n";
    file << "end_header\n";
    
    // Escribir vértices
    for (const auto& vertex : vertices) {
        file << vertex.x << " " << vertex.y << " " << vertex.z << "\n";
    }
    
    // Escribir caras
    for (const auto& face : faces) {
        file << "3 " << face[0] << " " << face[1] << " " << face[2] << "\n";
    }
    
    file.close();
}

void generate_mesh(function<double(double, double, double)> f, string output_filename,
                  double x_min, double y_min, double z_min,
                  double x_max, double y_max, double z_max,
                  double precision) {
    vector<triangle> triangles;
    recursive_marching_cubes(f, x_min, y_min, z_min, x_max, y_max, z_max, precision, triangles);
    cout << "Number of triangles: " << triangles.size() << endl;
    write_ply(output_filename, triangles);
}

